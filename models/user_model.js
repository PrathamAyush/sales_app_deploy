//creating usermodel/Schema for storing data in a format

const mongo = require("mongoose");
const { Schema } = mongo
const userSchema = new Schema({
    fullName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    tokens: [{
        token: {
            type: String,
            required: true
        }
    }]
});
mongo.model("userModel", userSchema);